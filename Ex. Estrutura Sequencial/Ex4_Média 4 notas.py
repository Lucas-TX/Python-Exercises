print('Exercício 4: média de 4 notas bimestrais. Método Sequencial')
primeiraNota = input('Informe a primeira nota bimestral: ')
segundaNota = input('Informe a segunda nota bimestral: ')
terceiraNota = input('Informe a terceira nota bimestral: ')
quartaNota = input('Informe a quarta nota bimestral: ')
media = (float(primeiraNota) + float(segundaNota) + float(terceiraNota) + float(quartaNota))/4
print('A média das notas é: ', media)
print('\n')
